    public void setData(Object data)
    {
        if(data == null)
        {
            booleanCombo.setSelectedIndex(0);
        }
        else
        {
            boolean bdata = false;
            boolean success = false;

            try
            {
                bdata = Boolean.parseBoolean((String)data);
                success = true;
            }
            catch(Exception e)
            {
                success = false;
                e.printStackTrace();
            }

            if(success)
            {
                if(bdata)
                {
                    booleanCombo.setSelectedIndex(0);
                }
                else
                {
                    booleanCombo.setSelectedIndex(1);
                }
            }
            else
            {
                log.debug("supplied data could not be cast as boolean data");
            }
        }
    }
